vector<int> encode(string s) {
	vector<int> d(988 * 988, 1);
	for (int i = 0; i < 200000; i += 5) {
		int ans = 0;
		for (int j = 0; j < 5; j++) {
			ans *= 27;
			if (i + j < sz(s))
				ans += (s[i + j] - 'a');
			else
				ans += 26;
		}
		for (int j = 0; j < 24; j++) {
			d[(i / 5) * 24 + j] = (ans >> j) % 2;
		}
	}
	return d;
}

string decode(vector<int> bs) {
	string ans;
	for (int i = 0; i < 40000; i++) {
		int b = 0;
		for (int j = 0; j < 24; j++) {
			b |= (bs[i * 24 + j] << j);
		}
		vector<int> seq;
		for (int i = 0; i < 5; i++) {
			seq.push_back(b % 27);
			b /= 27;
		}
		reverse(all(seq));
		bool ok = 1;
		for (auto &j : seq) {
			if (j >= 26) {
				ok = 0;
				break;
			} else
				ans.push_back(j + 'a');
		}
		if (!ok)
			return ans;
	}
	return ans;
}
